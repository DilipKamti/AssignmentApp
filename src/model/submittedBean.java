package model;

public class submittedBean 
{
 
	private String name;
	private String stream;
	private String year;
	private String assignDetail;
	private String assignDate;
	private String submissionDetail;
	private String submissionDate;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getStream() {
		return stream;
	}
	public void setStream(String stream) {
		this.stream = stream;
	}
	public String getYear() {
		return year;
	}
	public void setYear(String year) {
		this.year = year;
	}
	public String getAssignDetail() {
		return assignDetail;
	}
	public void setAssignDetail(String assignDetail) {
		this.assignDetail = assignDetail;
	}
	public String getAssignDate() {
		return assignDate;
	}
	public void setAssignDate(String assignDate) {
		this.assignDate = assignDate;
	}
	public String getSubmissionDetail() {
		return submissionDetail;
	}
	public void setSubmissionDetail(String submissionDetail) {
		this.submissionDetail = submissionDetail;
	}
	public String getSubmissionDate() {
		return submissionDate;
	}
	public void setSubmissionDate(String submissionDate) {
		this.submissionDate = submissionDate;
	}
	
}
