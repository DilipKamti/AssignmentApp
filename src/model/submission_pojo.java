package model;

public class submission_pojo 
{
   private String id;
   private String detail;
   private String tid;
   private String aid;
   
   
   
public String getTid() {
	return tid;
}
public void setTid(String tid) {
	this.tid = tid;
}
public String getId() {
	return id;
}
public void setId(String id) {
	this.id = id;
}
public String getDetail() {
	return detail;
}
public void setDetail(String detail) {
	this.detail = detail;
}
public String getAid() {
	return aid;
}
public void setAid(String aid) {
	this.aid = aid;
}

}
